package anketa.model;

public class Proizvodi {
	
	private int hrana;
	private int tehnika;
	private int odevniPredmeti;
	private int ostalo;
	
	public int getHrana() {
		return hrana;
	}
	public void setHrana(int hrana) {
		this.hrana = hrana;
	}
	public int getTehnika() {
		return tehnika;
	}
	public void setTehnika(int tehnika) {
		this.tehnika = tehnika;
	}
	public int getOdevniPredmeti() {
		return odevniPredmeti;
	}
	public void setOdevniPredmeti(int odevniPredmeti) {
		this.odevniPredmeti = odevniPredmeti;
	}
	public int getOstalo() {
		return ostalo;
	}
	public void setOstalo(int ostalo) {
		this.ostalo = ostalo;
	}
	
	
}
